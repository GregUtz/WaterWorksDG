	function pad(value){
		return String(value).padStart(2, '0');
	}

	function makeThursdaySeason(year){
		var weeks = [];
		var date = new Date(Date.UTC(year, 3, 1));
		while(date.getUTCDay() !== 4){
			date.setUTCDate(date.getUTCDate() + 1);
		}
		while(date.getUTCFullYear() === year && date.getUTCMonth() <= 8){
			if(!(date.getUTCMonth() === 6 && date.getUTCDate() === 4)){
				weeks.push({date:year + '-' + pad(date.getUTCMonth() + 1) + '-' + pad(date.getUTCDate())});
			}
			date.setUTCDate(date.getUTCDate() + 7);
		}
		return weeks;
	}

	function mergeWeeks(generatedWeeks, linkedWeeks){
		var byDate = {};
		generatedWeeks.forEach(function(week){
			byDate[week.date] = {date: week.date};
		});
		(linkedWeeks || []).forEach(function(week){
			byDate[week.date] = week;
		});
		return Object.keys(byDate).sort().map(function(date){ return byDate[date]; });
	}

	function numberWeeks(weeks){
		return weeks
			.slice()
			.sort(function(a, b){ return a.date.localeCompare(b.date); })
			.map(function(week, index){
				var copy = {};
				Object.keys(week).forEach(function(key){ copy[key] = week[key]; });
				copy.week = index + 1;
				return copy;
			});
	}

